import cmath
import math


def Add(x, y):
    return x + y


def Sub(x, y):
    return x - y


def Multi(x, y):
    return x * y


def Div(x, y):
    return x / y


def Pow(x, y):
    return x ** y


def FloorDiv(x, y):
    return x // y


def factorial(x):
    return math.factorial(x)


def cos(x):
    return cmath.cos(x)


def sin(x):
    return cmath.sin(x)


def tan(x):
    return cmath.tan(x)
